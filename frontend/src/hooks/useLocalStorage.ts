// 用户偏好设置 Hook
export interface UserPreferences {
  theme: 'light' | 'dark' | 'auto';
  language: 'zh-CN' | 'en-US';
  fontSize: 'small' | 'medium' | 'large';
  autoSave: boolean;
  showTips: boolean;
}

const defaultPreferences: UserPreferences = {
  theme: 'auto',
  language: 'zh-CN',
  fontSize: 'medium',
  autoSave: true,
  showTips: true
};

export const useUserPreferences = () => {
  return useLocalStorage<UserPreferences>('user_preferences', defaultPreferences);
};